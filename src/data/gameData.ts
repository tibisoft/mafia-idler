export function generateCrewName(): { name: string; nickname: string } {
  const first = NAMES.first[Math.floor(Math.random() * NAMES.first.length)];
  const last = NAMES.last[Math.floor(Math.random() * NAMES.last.length)];
  const nickname = NAMES.nicknames[Math.floor(Math.random() * NAMES.nicknames.length)];
  return { name: `${first} ${last}`, nickname };
}

